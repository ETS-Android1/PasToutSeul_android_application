public class Utilisateur {

    String email;
    String id_user;
    String username;
    String password;

    public Utilisateur(String id, String username, String email, String pwd)
    {
        this.id_user = id;
        this.email = email;
        this.username = username;
        this.email = email;
        this.password = pwd;
    }
}
